      void fillTable()
    {
        try
        {
        Vector heading=new Vector();
        heading.add("Coupon ID");
        heading.add("Coupon Name");
        heading.add("Percentage Discount");
        
        
        
        String query="select * from coupons2";
        PreparedStatement ps=con.prepareStatement(query);
        ResultSet rs=ps.executeQuery();
        Vector data=new Vector();
        while(rs.next())
        {
            Vector row=new Vector();
            row.add(rs.getString("coupon_id"));
            row.add(rs.getString("coupon_name"));
            row.add(rs.getString("discount"));
           
            
            data.add(row);
         }
            DefaultTableModel df=new DefaultTableModel(data,heading);
            CouponTable.setModel(df);
        
        }
        catch(Exception ex)
        {
            JOptionPane.showMessageDialog(this, "Error occured "+ex.getMessage());
            ex.printStackTrace();
        }
        
    }
    
    
    
