    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
       try {
            String cpid = txtcoupid.getText();
            String cpname = txtcoupname.getText();
            String percen = txtperc.getText();
           
            
            
            String sql = "insert into coupons2 (coupon_id, coupon_name,discount) values(?,?,?)";
            PreparedStatement p = con.prepareStatement(sql);
            p.setString(1, cpid);
            p.setString(2, cpname);
            p.setString(3, percen);
            
            
            p.executeUpdate();
            
            JOptionPane.showMessageDialog(this, "Data Saved");
            fillTable();
            
            
        } catch (SQLException ex) {
            Logger.getLogger(departments.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnAddActionPerformed

