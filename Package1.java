    void fillCategory() {
        try {
            String query = "select * from category";
            PreparedStatement ps = con.prepareStatement(query);

            ResultSet rs = ps.executeQuery();

            String category_array[] = new String[2];

            while (rs.next()) {
                category_array = new String[2];
                category_array[0] = rs.getString("category_id");
                category_array[1] = rs.getString("category_name");
                cmbcat.addItem(rs.getString("category_name"));
                categorylist.add(category_array);
            }
            flag = 1;

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    void fillTable(String catcode, String catname) {
        try {
            Vector heading = new Vector();
            heading.add("category_id");
            heading.add("category");
            heading.add("price");

            String query = "select * from subcategory where category_id =? ";
            PreparedStatement ps = con.prepareStatement(query);
            ps.setString(1, catcode);
            ResultSet rs = ps.executeQuery();

            int price = 0;
            while (rs.next()) {
                price += Integer.parseInt(rs.getString("price"));

            }

            Vector row = new Vector();
            row.add(catcode);
            row.add(catname);
            row.add(price);
            data.add(row);
            DefaultTableModel df = new DefaultTableModel(data, heading);
            jTable1.setModel(df);

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error occured " + ex.getMessage());
            ex.printStackTrace();
        }

    }

