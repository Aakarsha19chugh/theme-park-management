    void filltable() {

        try {
            Vector heading = new Vector();
            heading.add("Package ID");
            heading.add("Package Name");
            heading.add("Total Cost");

            String query = "select * from package1";
            PreparedStatement ps = con.prepareStatement(query);
            ResultSet rs = ps.executeQuery();
            Vector data = new Vector();
            while (rs.next()) {
                Vector row = new Vector();
                row.add(rs.getString("pack_id"));
                row.add(rs.getString("package_name"));
                row.add(rs.getString("total_price"));

                data.add(row);
            }
            DefaultTableModel df = new DefaultTableModel(data, heading);
            packagetable.setModel(df);

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error occured " + ex.getMessage());
            ex.printStackTrace();
        }
    }

